class CharLit
{
	public static void main(String[] args)
	{
		char arr[][]={{'v','i','j','a','y'},{'a','j','i','t','h'},{'k','a','m','a','l'},{'s','i','m','b','u'}};
		
		for(int i=0;i<arr.length;i++)
		{
			for(int j=0;j<arr[i].length;j++)
			{
				System.out.print(arr[i][j]+ "  ");
			}
				System.out.println("");
			
		}
	}	
}