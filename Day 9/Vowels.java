class Vowels
{
	public static void main(String[] args)
	{
		char c='a';
		char c2='i';
		char c3='z';
		
		if(c=='a' || c=='e' || c=='i' || c=='o' || c=='u')
			System.out.println(c+" is vowels");
		else
			System.out.println(c+" is consonant");
		
		if(c2=='a' || c2=='e' || c2=='i' || c2=='o' || c2=='u')
			System.out.println(c2+" is vowels");
		else
			System.out.println(c2+" is consonant");
		
		if(c3=='a' || c3=='e' || c3=='i' || c3=='o' || c3=='u')
			System.out.println(c3+" is vowels");
		else
			System.out.println(c3+" is consonant");
	}
}