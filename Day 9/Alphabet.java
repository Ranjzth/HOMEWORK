class Alphabet
{
	public static void main(String[] args)
	{
		char a='a';
		char b='g';
		int c=5;
		if(a>='a' && a<='z')
			System.out.println(a+" is alphabet");
		else
			System.out.println(a+" is not alphabet");
		
		if(b>='a' && a<='z')
			System.out.println(b+" is alphabet");
		else
			System.out.println(b+" is not alphabet");
		
		if(c>='a' && a<='z')
			System.out.println(c+" is alphabet");
		else
			System.out.println(c+" is not alphabet");
	}
}